/** Request Type */
public enum RequestType {
    
    /** GET request */
    case Get
    
    /** PUT (edit) request */
    case Edit
    
    /** DELETE request */
    case Delete
    
    /** POST (create new) request */
    case Create
    
    /** Search request */
    case Search
    
    /** Function request */
    case Function
}